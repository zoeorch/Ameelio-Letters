                    <h3>PROBLEM</h3>
                    <span>Nearly 10% of Americans (27 million people) have a family member or friend that is currently incarcerated. And although the U.S. spends over $80 billion annually on the corrections system, loved ones are forced to spend an additional $4 billion per year supporting the incarcerated. This is primarily due to the exploitative nature of the prison industry.</span>
                    <br><br>
                    <span>Since the 1990’s, a small group of private companies have made billions charging incarcerated people and their loved ones extortionate prices to stay connected. Currently, the prison telecommunications industry, dominated by just two companies, is valued at over $1.2 billion dollars. In contrast, the average family of an incarcerated person is $13,000 in debt, yet are forced to pay up to $1.22 per minute just to call a loved one in prison. That is more than 25 times what a non-incarcerated person pays for a phone call.</span>
                    <br><br>
                    <span>Research has confirmed that greater contact between the incarcerated and their loved ones improves post-release outcomes, and significantly reduces re-offending. Yet because of the financial barriers placed on many incarcerated persons and their families, within the first five-years of being released more than 50% of re-entrants go on to reoffend.</span>
